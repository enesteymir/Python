print("Merhaba arabası olan zengin kardeş. Arabanın km total masrafını hesaplayalım")

h= float(input("Aracın km başına ne kadar yakıyor hacı: "))
km = int(input("Bu zamana dek aracın toplam kaç km yaptı? :"))

masraf = h * km

print("Senin toplam ödemen gereken bedel aha da bu kadar: {} TL".format(masraf))